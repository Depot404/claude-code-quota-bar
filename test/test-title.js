// Banc du titre de conversation (lot 6e) : aucun balisage de CLI ne doit
// atteindre le panneau. Deux niveaux : la règle seule (cleanTitle), puis
// extractTitleInfo sur de VRAIS transcripts fabriqués — dont une reproduction
// littérale du cas signalé (conv ouverte par « /model opus[1m] »).
const fs = require('fs');
const os = require('os');
const path = require('path');

const { cleanTitle, extractTitleInfo } = require(path.join(__dirname, '..', 'hooks', 'transcript.js'));

let pass = 0, fail = 0;
function check(name, cond, detail) {
  if (cond) { pass++; console.log(`  ok   ${name}`); }
  else { fail++; console.log(`  FAIL ${name}${detail ? ' → ' + detail : ''}`); }
}

console.log('\n1. Règle de nettoyage (cleanTitle)');
check('slash-command : n\'est QUE du balisage → aucun titre (on passe au suivant)',
  cleanTitle('<command-name>/model</command-name>\n            <command-message>model</command-message>\n            <command-args>opus[1m]</command-args>') === null,
  JSON.stringify(cleanTitle('<command-name>/model</command-name> <command-args>opus</command-args>')));
check('sortie de commande locale → aucun titre',
  cleanTitle('<local-command-stdout>Set model to claude-opus-4-8[1m]</local-command-stdout>') === null);
check('caveat de commande locale → aucun titre',
  cleanTitle('<local-command-caveat>Caveat: The messages below…</local-command-caveat>') === null);
check('system-reminder (n\'importe où) retiré, le prompt survit',
  cleanTitle('<system-reminder>bla bla</system-reminder>Corriger le watchdog') === 'Corriger le watchdog',
  JSON.stringify(cleanTitle('<system-reminder>bla</system-reminder>Corriger le watchdog')));
check('enveloppe en tête + vrai prompt derrière → seul le prompt reste',
  cleanTitle('<ide_selection>x = 1</ide_selection> Pourquoi ce test échoue ?') === 'Pourquoi ce test échoue ?',
  JSON.stringify(cleanTitle('<ide_selection>x = 1</ide_selection> Pourquoi ce test échoue ?')));
check('balise INCONNUE en tête (règle générique, pas de liste) → retirée',
  cleanTitle('<future-cli-tag>zzz</future-cli-tag>Refonte du digest') === 'Refonte du digest',
  JSON.stringify(cleanTitle('<future-cli-tag>zzz</future-cli-tag>Refonte du digest')));
check('balise ouvrante jamais refermée → dépouillée (aucun < > à l\'écran)',
  cleanTitle('<command-name>/model') === '/model',
  JSON.stringify(cleanTitle('<command-name>/model')));

console.log('\n2. Ce qui NE doit PAS être touché');
check('prompt humain parlant de code : les chevrons du milieu restent',
  cleanTitle('pourquoi ce <div> déborde ?') === 'pourquoi ce <div> déborde ?',
  JSON.stringify(cleanTitle('pourquoi ce <div> déborde ?')));
check('comparaison numérique (< suivi d\'un chiffre) intacte',
  cleanTitle('garder la latence < 1s au clic') === 'garder la latence < 1s au clic',
  JSON.stringify(cleanTitle('garder la latence < 1s au clic')));
// La coupe d'affichage est du ressort du CSS (constat user 2026-07-15 : titre
// tronqué même panneau élargi) — ici seule reste une garde de salubrité à 200
// pour les titres de repli (prompts collés entiers).
check('titre moyen (80 car.) transmis ENTIER — plus de coupe à 40',
  cleanTitle('a'.repeat(80)) === 'a'.repeat(80),
  JSON.stringify(cleanTitle('a'.repeat(80))));
check('garde de salubrité à 200 car. pour les pavés',
  cleanTitle('a'.repeat(500)) === 'a'.repeat(199) + '…',
  JSON.stringify((cleanTitle('a'.repeat(500)) || '').length));

// ── Sur de vrais fichiers ─────────────────────────────────────────────────
console.log('\n3. extractTitleInfo sur transcripts fabriqués');
const SANDBOX = fs.mkdtempSync(path.join(os.tmpdir(), 'qb-title-'));
function write(name, lines) {
  const p = path.join(SANDBOX, name);
  fs.writeFileSync(p, lines.map((l) => JSON.stringify(l)).join('\n') + '\n');
  return p;
}
const user = (txt, extra = {}) => ({ type: 'user', message: { content: txt }, ...extra });

// Reproduction LITTÉRALE du cas signalé : /model puis sa sortie, puis le prompt.
// (Relevé le 2026-07-15 dans un transcript réel de ce workspace.)
const slash = write('slash.jsonl', [
  user('<local-command-caveat>Caveat: The messages below were generated by the user while running local commands.</local-command-caveat>', { isMeta: true }),
  user('<command-name>/model</command-name>\n            <command-message>model</command-message>\n            <command-args>opus[1m]</command-args>'),
  user('<local-command-stdout>Set model to claude-opus-4-8[1m]</local-command-stdout>'),
  user('Implémente le lot 6 du plan QuotaBar'),
  { type: 'assistant', message: { model: 'claude-opus-4-8', usage: { input_tokens: 10 } } },
]);
let info = extractTitleInfo(slash);
check('conv ouverte par /model → titre = le vrai prompt humain',
  info.title === 'Implémente le lot 6 du plan QuotaBar', JSON.stringify(info));
check('… et jamais de balisage à l\'écran', !/[<>]/.test(info.title || ''), JSON.stringify(info.title));
check('… provenance first-user (donc jamais masquée par le filtre du lot 5)',
  info.source === 'first-user', JSON.stringify(info));

// ai-title reste prioritaire, et n'est pas affecté par le nettoyage.
const titled = write('titled.jsonl', [
  user('<command-name>/model</command-name>'),
  user('peu importe'),
  { type: 'ai-title', aiTitle: 'Lot 6 — deux teintes du check' },
]);
info = extractTitleInfo(titled);
check('ai-title garde la priorité', info.title === 'Lot 6 — deux teintes du check' && info.source === 'ai-title',
  JSON.stringify(info));

// Conv qui n'est QUE des slash-commands : aucun titre humain à trouver.
const onlySlash = write('only.jsonl', [
  user('<command-name>/clear</command-name>'),
  user('<local-command-stdout>ok</local-command-stdout>'),
]);
info = extractTitleInfo(onlySlash);
check('conv sans aucun texte humain → pas de titre (repli « Conversation », pas de balisage)',
  info.title === null && info.source === null, JSON.stringify(info));

fs.rmSync(SANDBOX, { recursive: true, force: true });
console.log(`\n${pass} ok, ${fail} fail`);
process.exit(fail ? 1 : 0);
